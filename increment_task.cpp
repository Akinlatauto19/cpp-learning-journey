#include <iostream>

using namespace std;

int main()
{
    int counter = 5;

    cout << "initial number:" << counter << endl;
    counter++;
    cout << "first increment:" << counter << endl;
    counter++;
    cout << "second increment:" << counter << endl;
    counter--;
    cout << "first decrement:" << counter << endl;
    

    return 0;
}